    /**
     * Realiza una búsqueda cuando se pulsa ENTER en el buscador
     * @param evt 
     */
    private void jTextFieldBuscadorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldBuscadorKeyTyped
        if ((evt.getKeyChar() == '\n') && gestion.isConnected()){
            listaClientes = gestion.busqueda(tipoBusqueda, jTextFieldBuscador.getText());
            modeloListaClientes.clear();   
            if (listaClientes != null){
                int contador = 0;
                while (contador < listaClientes.size() ){
                    modeloListaClientes.addElement(listaClientes.get(contador)[0]);
                    contador++;
                }
            }
        }
    }//GEN-LAST:event_jTextFieldBuscadorKeyTyped
