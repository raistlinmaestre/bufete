    /**
     * Si cambia el cliente seleccionado de la lista de clientes, realiza la
     * búsqueda de los datos y el primer procedimiento del mismo
     * @param evt 
     */
    private void jListClientesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListClientesValueChanged
        if ((evt.getValueIsAdjusting() == false) && gestion.isConnected()){
            String identificador = new String();
            identificador = listaClientes.get(jListClientes.getSelectedIndex())[1];
            clienteActual = gestion.busquedaDatos(identificador);
            if (clienteActual.getNombre() != null)
                jTextFieldNombre.setText(clienteActual.getNombre());
            else
                jTextFieldNombre.setText("");
            if (clienteActual.getApellido1() != null)
                jTextFieldApellido1.setText(clienteActual.getApellido1());
            else
                jTextFieldApellido1.setText("");
            if (clienteActual.getApellido2() != null)
                jTextFieldApellido2.setText(clienteActual.getApellido2());
            else
                jTextFieldApellido2.setText("");
            if (clienteActual.isJuridica()){
                if (!jCheckBoxJuridica.isSelected())
                    jCheckBoxJuridica.doClick();
            }
            else{
                if (jCheckBoxJuridica.isSelected())
                    jCheckBoxJuridica.doClick();
            }   
            jTextFieldIdentificador.setText(clienteActual.getIdentificador().toString());
            if (clienteActual.getDomicilio() != null) {
                jTextFieldDomicilio.setText(clienteActual.getDomicilio().toString());
                jTextFieldLocalidad.setText(clienteActual.getDomicilio().getLocalidad());
                        //+ "(" + clienteActual.getDomicilio().getProvincia() + ")"); TODO
                jTextFieldCodigo.setText(Integer.toString(clienteActual.getDomicilio().getCodigo()));
            } else {
                jTextFieldDomicilio.setText("");
                jTextFieldLocalidad.setText("");
                jTextFieldCodigo.setText("");
            }
            if (clienteActual.getAnotaciones() != null)
                jTextAreaAnotacionesCliente.setText(clienteActual.getAnotaciones());
            else
                jTextAreaAnotacionesCliente.setText("");
            // Primer procedimiento
            if (clienteActual.getListaProcedimientos() != null){
                int contador = 0;
                while (contador < clienteActual.getListaProcedimientos().size()) {
                    jComboBoxProcedimiento.addItem(clienteActual.getProcedimiento(contador).getNig());
                    contador++;
                }
                jTextFieldNumeroProcedimiento.setText(clienteActual.getProcedimiento(0).getNig());
                jTextFieldJuzgado.setText(clienteActual.getProcedimiento(0).getJuzgado());
                jTextFieldTipoProcedimiento.setText(clienteActual.getProcedimiento(0).getTipoDeProcedimiento());
                if (clienteActual.getProcedimiento(0).isAbierto()){
                    if (!jCheckBoxAbierto.isSelected())
                        jCheckBoxAbierto.doClick();
                } else {
                    if (jCheckBoxAbierto.isSelected())
                        jCheckBoxAbierto.doClick();
                }
                jTextFieldMinuta.setText("" + clienteActual.getProcedimiento(0).getMinuta() + "€");
                jTextFieldPagado.setText("" + clienteActual.getProcedimiento(0).getPagado() + "€");
                jTextAreaAnotacionesProcedimiento.setText(clienteActual.getProcedimiento(0).getAnotaciones());
            } else {
                jComboBoxProcedimiento.removeAllItems();
                jTextFieldNumeroProcedimiento.setText("");
                jTextFieldJuzgado.setText("");
                jTextFieldTipoProcedimiento.setText("");
                if (jCheckBoxAbierto.isSelected())
                    jCheckBoxAbierto.doClick();
                jTextFieldMinuta.setText("");
                jTextFieldPagado.setText("");
                jTextAreaAnotacionesProcedimiento.setText("");
            }
        }
    }//GEN-LAST:event_jListClientesValueChanged
    /**
     * Busca los datos del procedimiento seleccionado
     * 
     * @param evt 
     */
    private void jComboBoxProcedimientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxProcedimientoActionPerformed
        if (clienteActual.getListaProcedimientos() != null){
            jTextFieldNumeroProcedimiento.setText(""
                    + clienteActual.getProcedimiento(jComboBoxProcedimiento.getSelectedIndex()).getNig());
            jTextFieldJuzgado.setText(""
                    + clienteActual.getProcedimiento(jComboBoxProcedimiento.getSelectedIndex()).getJuzgado());
            jTextFieldTipoProcedimiento.setText(""
                    + clienteActual.getProcedimiento(jComboBoxProcedimiento.getSelectedIndex()).getTipoDeProcedimiento());
            if (clienteActual.getProcedimiento(jComboBoxProcedimiento.getSelectedIndex()).isAbierto()){
                if (!jCheckBoxAbierto.isSelected())
                    jCheckBoxAbierto.doClick();
            } else {
                if (jCheckBoxAbierto.isSelected())
                    jCheckBoxAbierto.doClick();
            }
            jTextFieldMinuta.setText(""
                    + clienteActual.getProcedimiento(jComboBoxProcedimiento.getSelectedIndex()).getMinuta() + "€");
            jTextFieldPagado.setText(""
                    + clienteActual.getProcedimiento(jComboBoxProcedimiento.getSelectedIndex()).getPagado() + "€");
            jTextFieldExpediente.setText(""
                    + clienteActual.getProcedimiento(jComboBoxProcedimiento.getSelectedIndex()).getExpediente());
            jTextFieldFecha.setText(""
                    + clienteActual.getProcedimiento(jComboBoxProcedimiento.getSelectedIndex()).getFecha());
            jTextAreaAnotacionesProcedimiento.setText(""
                    + clienteActual.getProcedimiento(jComboBoxProcedimiento.getSelectedIndex()).getAnotaciones());
        }
    }//GEN-LAST:event_jComboBoxProcedimientoActionPerformed
    /**
     * Actualiza los datos del cliente al hacer clic en el botón
     * 
     * @param evt 
     */
    private void jButtonActualizarDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonActualizarDatosMouseClicked
        if (gestion.isConnected()) {
            String nombre = jTextFieldNombre.getText();
            String apellido1 = jTextFieldApellido1.getText();
            String apellido2 = jTextFieldApellido2.getText();
            String nif = jTextFieldIdentificador.getText();
            boolean juridica = jCheckBoxJuridica.isSelected();
            String domicilio = jTextFieldDomicilio.getText();
            String localidad = jTextFieldLocalidad.getText();
            String codigo = jTextFieldCodigo.getText();
            gestion.updateCliente(nombre, apellido1, apellido2, nif, juridica, domicilio, localidad, codigo, nif);
        }
    }//GEN-LAST:event_jButtonActualizarDatosMouseClicked
    /**
     * Actualiza los datos del procedimiento al hacer clic en el botón
     * 
     * @param evt 
     */
    private void jButtonActualizarProcedimientoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonActualizarProcedimientoMouseClicked
        if (gestion.isConnected()) {
            String numero = jTextFieldNumeroProcedimiento.getText();
            String juzgado = jTextFieldJuzgado.getText();
            String tipoProcedimiento = jTextFieldTipoProcedimiento.getText();
            boolean abierto = jCheckBoxAbierto.isSelected();
            String minutaString = jTextFieldMinuta.getText();
            minutaString = minutaString.substring(0,minutaString.length()-1);
            float minuta = Float.parseFloat(minutaString);
            String pagadoString = jTextFieldPagado.getText();
            pagadoString = pagadoString.substring(0,pagadoString.length()-1);
            float pagado = Float.parseFloat(pagadoString);
            String fecha = jTextFieldFecha.getText();
            String procurador = jTextFieldProcurador.getText();
            String procuradorContrario = jTextFieldProcuradorContrario.getText();
            String anotaciones = jTextAreaAnotacionesProcedimiento.getText();
        
            gestion.updateProcedimiento(numero, juzgado, tipoProcedimiento, abierto, minuta,
                    pagado, fecha, procurador, procuradorContrario, anotaciones);
        }
    }//GEN-LAST:event_jButtonActualizarProcedimientoMouseClicked
